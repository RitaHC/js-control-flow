const age = 19

//Telling stages of life as per age

if (age>0 && age<=1){
    console.log(`Infant age -> ${age}`)
} else if(age >=2 && age<=4){
    console.log(`Toddler age -> ${age}`)
} else if(age >=5 && age<=12){
    console.log(`Child age -> ${age}`)
} else if (age >= 13 && age <=15){
    console.log(`Pre-teen age -> ${age}`)
} else if (age >=16 && age <=17){
    console.log(`Teen age -> ${age}`)
} else if (age>= 18 && age<=20){
    console.log(`Young adult age -> ${age}`)
} else {
    console.log(`invalid age entered for project`)
}

